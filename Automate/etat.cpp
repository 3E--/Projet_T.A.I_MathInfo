bool Etat::operator ==(Etat const& etat)const{
    /******************************
     * SURCHARGE DE L'OPERATEUR ==
     * ****************************/

    // ON COMPARE LE NOM DES 2 ETATS EN ARGUMENT
    if (nom==etat.getNom())return true;
    else return false;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool Etat::operator ==(string const& nomEtat)const{
    /******************************
     * SURCHARGE DE L'OPERATEUR ==
     * ****************************/

    // ON COMPARE LE NOM DES 2 ETATS EN ARGUMENT
    if (nom==nomEtat)return true;
    else return false;
}
